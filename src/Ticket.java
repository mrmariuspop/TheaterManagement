
public class Ticket {
public String showTitle;
public int row;
public int col;

public String getShowTitle() {
	return showTitle;
}
public void setShowTitle(String showTitle) {
	this.showTitle = showTitle;
}
public int getRow() {
	return row;
}
public void setRow(int row) {
	this.row = row;
}
public int getCol() {
	return col;
}
public void setCol(int col) {
	this.col = col;
}
}
